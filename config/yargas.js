const opt = {
    base: {
        demand: true,
        alias: 'b'
    },
    limite: {
        alias: 'l',
        default: 10
    }
}

const argv = require('yargs')
    .command('listar', 'Imprime en consola la tabla de multiplicar', opt)
    .command('crear', 'crear un archivo con la tabla de multiplicar del valor Base con un limite ', opt)
    .help()
    .argv

module.exports = {
    argv
}